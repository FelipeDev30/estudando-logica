/* Para que server lógica? */

/* Lógica é a base de tudo na programação. Ela nos permite criar instruções que o computador pode seguir para realizar tarefas específicas. Com lógica, podemos tomar decisões, repetir ações e manipular dados de maneira eficiente. Sem lógica, não seria possível desenvolver programas funcionais e úteis. */

/* Exemplos práticos de lógica na programação: */ 
// 1. Condicionais (if, else)
let idade = 18;
if (idade >= 18) {
    console.log("Você é maior de idade.");
} else {
    console.log("Você é menor de idade.");
}

// 2. Loops (for, while)
for (let i = 0; i < 5; i++) {
    console.log("Número: " + i);
}

// 3. Funções
function somar(a, b) {
    return a + b;
}
console.log(somar(3, 4)); // Output: 7

// 4. Manipulação de arrays
let numeros = [1, 2, 3, 4, 5];
numeros.forEach(function(numero) {
    console.log("Número: " + numero);
});

/* Em resumo, a lógica é essencial para criar programas que funcionam corretamente e atendem às necessidades dos usuários. Ela nos ajuda a estruturar nosso código de maneira clara e eficiente. */