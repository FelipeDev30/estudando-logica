/* 

Matrizes (Arrays Multidimensionais)
Uma matriz é uma coleção de arrays, onde cada array pode conter múltiplos valores. 
Elas são úteis para armazenar dados estruturados, como tabelas ou listas de informações relacionadas.

*/

let pokemons = [
    ['Pikachu', 'M', 'Level 25'],
    ['Squirtle', 'F', 'Level 30'],
    ['Psyduck', 'M', 'Level 20']
]

console.log("Matriz de Pokémons:");
console.log(pokemons[0][0], "-", pokemons[0][1], "-", pokemons[0][2]);
console.log(pokemons[1][0], "-", pokemons[1][1], "-", pokemons[1][2]);
console.log(pokemons[2][0], "-", pokemons[2][1], "-", pokemons[2][2]);

console.log('------------------------------------------------');

