/* 

ILanches - Sistema de Pedido de Comida 

Comandos de entrada: Input são instruções que o sistema recebe do usuário (entrada de dados) para iniciar um processo.

- procura um pedido
- solicita um pedido
- solicitar cancelamento do pedido

Comandos de processamento: Process são as operações que o sistema realiza com base nos comandos de entrada para gerar um resultado.

- Verifica se o restaurante está aberto
- Valida o pedido
- Calcula o total do pedido
- Processa o pagamento:
    - Verifica se o pagamento foi aprovado
- Começa a preparar o pedido
- Cancela o pedido
    - Verificar se o pedido está atrasado
    - Verificar se terá multa por atraso
    - Gerar cancelamento do pedido

Comandos de saída: Output são as respostas ou resultados que o sistema fornece ao usuário após o processamento dos comandos de entrada.

- Notifica o cliente sobre o status do pedido
- Envia o pedido para entrega

*/